<?php
$a = 20;
$b = "20"; // تم تعريف b كنص (String)

var_dump($a == $b);   // مقارنة القيمة فقط (الناتج true لأن كلاهما 20)
var_dump($a === $b);  // مطابقة القيمة ونوع البيانات (الناتج false لاختلاف النوع: رقم ضد نص)
var_dump($a != $b);   // لا يساوي في القيمة (الناتج false لأن القيمتين متساويتان)

$c = 30;
$d = 25;
var_dump($c > $d);    // هل C أكبر من D؟ (الناتج true)
var_dump($c < $d);    // هل C أصغر من D؟ (الناتج false)
var_dump($c >= $d);   // هل C أكبر من أو يساوي D؟ (الناتج true)
var_dump($c <= $d);   // هل C أصغر من أو يساوي D؟ (الناتج false)
?>