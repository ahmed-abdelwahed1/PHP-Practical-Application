<?php
$a = 100;
$b = 50;

// AND أو &&: يجب تحقق جميع الشروط ليتنفذ الأمر
if ($a == 100 and $b == 50) { echo "حور"; }

// OR أو ||: يكفي تحقق شرط واحد فقط ليتنفذ الأمر
if ($a == 100 or $b == 10) { echo "حور"; }

// XOR: يتنفذ الأمر إذا تحقق شرط واحد فقط من الشروط، وليس كلاهما معاً
if ($a == 100 xor $b == 10) { echo "حور"; }

// NOT أو (!): يتنفذ الأمر إذا كان الشرط غير متحقق (عكس الشرط)
if ($a != 300) { echo "حور"; } // بما أن a لا تساوي 300، فسيطبع الكلمة
?>