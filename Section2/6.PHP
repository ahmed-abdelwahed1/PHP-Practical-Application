<?php
$a = 20;

echo ++$a; // زيادة 1 للمتغير أولاً ثم الطباعة (سيطبع 21)
echo $a++; // طباعة القيمة الحالية أولاً (سيطبع 21)، ثم يضيف 1 في الذاكرة وتصبح 22
echo $a;   // طباعة القيمة الأخيرة للمتغير لمعاينة الزيادة المخفية (سيطبع 22)

echo --$a; // طرح 1 من المتغير أولاً ثم الطباعة (سيطبع 21)
echo $a--; // طباعة القيمة الحالية أولاً (سيطبع 21)، ثم يطرح 1 في الذاكرة وتصبح 20
echo $a;   // طباعة القيمة الأخيرة للمتغير (سيطبع 20)
?>