<?php
$a = 20;

$a += 5;   // إضافة 5 للقيمة الحالية (تصبح القيمة 25)
echo $a . "<br>";
$a -= 5;   // طرح 5 من القيمة الحالية (تعود القيمة 20)
echo $a . "<br>";
$a *= 3;   // ضرب القيمة الحالية في 3 (تصبح القيمة 60)
echo $a . "<br>";
$a /= 3;   // قسمة القيمة الحالية على 3 (تعود القيمة 20)
echo $a . "<br>";
$a %= 7;   // باقي قسمة القيمة الحالية على 7 (تصبح القيمة 6)
echo $a . "<br>";
$a **= 2;  // رفع القيمة الحالية للأُس 2 (تصبح القيمة 36)
echo $a . "<br>";
?>